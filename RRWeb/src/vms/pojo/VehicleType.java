package vms.pojo;

public class VehicleType {

	int vehicle_type_id ;
	String vehicle_type_name;
	String vehicle_type_status;
	public int getVehicle_type_id() {
		return vehicle_type_id;
	}
	public void setVehicle_type_id(int vehicle_type_id) {
		this.vehicle_type_id = vehicle_type_id;
	}
	public String getVehicle_type_name() {
		return vehicle_type_name;
	}
	public void setVehicle_type_name(String vehicle_type_name) {
		this.vehicle_type_name = vehicle_type_name;
	}
	public String getVehicle_type_status() {
		return vehicle_type_status;
	}
	public void setVehicle_type_status(String vehicle_type_status) {
		this.vehicle_type_status = vehicle_type_status;
	}
	
	
	
}
